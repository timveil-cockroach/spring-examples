package io.crdb.spring;

import org.junit.jupiter.api.Test;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.context.SpringBootTest;
import org.springframework.test.context.ActiveProfiles;

@SpringBootTest
@ActiveProfiles("test")
public class BusinessServiceIT {

    private static final Logger logger = LoggerFactory.getLogger(BusinessServiceIT.class);

    private final BusinessService businessService;
    private final UserBuilder userBuilder;

    @Autowired
    public BusinessServiceIT(BusinessService businessService, UserBuilder userBuilder) {
        this.businessService = businessService;
        this.userBuilder = userBuilder;
    }

    @Test
    void retryWithRetry() {
        User test = userBuilder.buildUser();

        logger.debug(test.toString());

        businessService.someComplexService(test, true);

    }

    @Test
    void retryWithoutRetry() {
        User test = userBuilder.buildUser();

        logger.debug(test.toString());

        businessService.someComplexService(test, false);
    }
}
